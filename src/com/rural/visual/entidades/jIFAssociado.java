    private void validateData(JFormattedTextField data) {
        String aux = data.getText();
        aux = aux.replaceAll("/", "");
        aux = aux.trim();

        if (!ValidatorUtil.isNullOrEmpty(aux)) {
            if (!ValidatorUtil.isValidDate(data.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Data em formato inválido.");
                data.setText("");
            }
        }
    }

    private boolean validateCamposCadastro() {
        StringBuilder erros = new StringBuilder();
        if (ValidatorUtil.isNullOrEmpty(jTFNome.getText())) {
            erros.append("O Nome não foi preenchido.\n");
        }

        String cpf = jFTFCPF.getText().replace(".", "").replace("-", "").trim();

        if (ValidatorUtil.isNullOrEmpty(cpf)) {
            erros.append("O CPF não foi preenchido.\n");
        }

        if (erros.length() > 0) {
            JOptionPane.showMessageDialog(rootPane, "O seguintes problemas foram encontrados:\n" + erros.toString());
            return false;
        }

        return true;
    }

